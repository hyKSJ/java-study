package prob01;

public class Printer {
	public void println(int message) {
		System.out.println(message);
	}
	public void println(double message) {
		System.out.println(message);
	}
	public void println(boolean message) {
		System.out.println(message);
	}
	public void println(String message) {
		System.out.println(message);
	}
}
